package com.example.dndapp;

public class SpellCard {

    private String id;
    private String name;


    public SpellCard() {

    }

    public void setId(String id) {
        this.id = id;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }
}

